package collections.arraylist;

/**
 *
 * @author Nicholas Christiny
 * @param data array of int value
 * @param next reference to next node
 */
public class Node {

    int data;
    Node next;
    
    public Node(int v) {
        data = v;
        next = null;
    }       
}
